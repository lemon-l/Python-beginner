#进行 按位或 操作
a = 9      # a = 0000 1001
b = a | 2  # b = 0000 1001 | 0000 0010 = 0000 1011 = 11
c = a | 3  # c = 0000 1001 | 0000 0011 = 0000 1011 = 11
d = a | 4  # d = 0000 1001 | 0000 0100 = 0000 1101 = 13
print('a & 2 = %d' % b)
print('a & 3 = %d' % c)
print('a & 4 = %d' % d)
print('---------------')
